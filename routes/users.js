const express = require('express');
const userService = require('../services/users');
const cookieParser = require('cookie-parser');
const gameService = require('../services/game');

const router = express.Router();

router.get('/', function (req, res, next) {
    // List all users - NO AUTH AT PRESENT
    let db = req.app.get('super6db');
    let collection = db.collection('users');
    let items = collection.find({}).toArray(function (err, docs) {
        if (err) {
            console.error(err)
        }
        res.json(docs)
    });
});

router.post('/signup', function (req, res, next) {
    // Save a user - NO AUTH AT PRESENT
    let email = req.body.email;
    let password = req.body.password;
    let userId = userService.createUser(req.app.get('super6db'), email, password, false, function (error, result) {
        res.cookie('super6token', 'abcd1234', { maxAge: 3600000 });
        res.redirect('../');
    }, function () {
        res.statusCode = 500;
        res.send('Error adding user');
    });

});

router.post('/login/', function (req, res, next) {
    let email = req.body.email;
    let password = req.body.password;
    if (email === 'user@gmail.com' && password === 'password') { // to be replaced with call to the user service
        res.cookie('super6token', 'abcd1234', { maxAge: 3600000 }); // to be replaced with the session token generated by the service
        res.redirect('../');
    } else {
        res.send('Incorrect details');
    }
});

router.get('/logout', function (req, res, next) {
    // Delete token from database via user service
    res.clearCookie('super6token');
    res.redirect('../');
});

router.get('/play', function (req, res, next) {
    gameService.fetchFuture(req.app).then((games) => {
        res.render('play', {
            title: 'Super6 Rugby - Play', games: games, testValue: "HELLO WORLD", loggedIn: true
        }); // TODO: Logged in needs to reflect cookie value and checked against db and games need to be pushed
    },
        (reason) => {
            console.log(reason)
        });
});

router.get('/history', function (req, res, next) {
    res.render('history', { title: 'Super6 Rugby - Your History', loggedIn: true });
});

module.exports = router;
